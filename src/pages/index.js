import Head from 'next/head'
import Image from 'next/image'
import axios from 'axios'
import { useState } from 'react'
import { BsSearch } from 'react-icons/bs'
import background from '../assets/images/background.jpg'


export default function Home() {

  const [city, setCity] = useState('')
  const [weather, setWeather] = useState({})
  const [loading, setLoading] = useState(false)

  const url = `https://api.openweathermap.org/data/2.5/weather?q=toronto&units=metric&appid=${process.env.NEXT_PUBLIC_WEATHER_KEY}`

  const getWeather = (event) => {
    event.preventDefault()
    setLoading(true)
    axios.get(url)
      .then((response) => {
        setWeather(response.data)
        console.log(response.data)
      })
    setCity('')
    setLoading(true)
  }

  return (
    <div className='relative'>
      <Head>
        <title>Weather App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.png" />
      </Head>
     <div className='absolute'>

  
   
      
      <div className='absolute flex justify-between items-center max-w-[500px] w-full m-auto pt-4 px-4 bg-red z-50'>
        <form>
          <div>
            <input type="text" placeholder="Search city" />
          </div>
          <button onClick={getWeather}><BsSearch /></button>
        </form>
      </div>

     
      </div>

      {/* <div className='absolute top-0 left-0 right-0 bottom-0 bg-black/40 z-[1]' /> */}


    </div>
  )
}
